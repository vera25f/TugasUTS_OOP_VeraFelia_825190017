public class Kasir {

    private int harga;
    private int banyak;
    private int total;
    private char yesorno;
    private int toppings;
    private char piltoping;
    private double discount;


    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public int getBanyak() {
        return banyak;
    }

    public void setBanyak(int banyak) {
        this.banyak = banyak;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public char getYesorno() {
        return yesorno;
    }

    public void setYesorno(char yesorno) {
        this.yesorno = yesorno;
    }

    public int getToppings() {
        return toppings;
    }

    public void setToppings(int toppings) {
        this.toppings = toppings;
    }

    public char getPiltoping() {
        return piltoping;
    }

    public void setPiltoping(char piltoping) {
        this.piltoping = piltoping;
    }

    public double getDiscount() {
        return discount;
    }

    public void setDiscount(double discount) {
        this.discount = discount;
    }
}
